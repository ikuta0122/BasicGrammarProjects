package com.ikuta.demo;

/*关系运算符
>
>=
<
<=
==
！=
*/
public class OperatorDemo02 {
	public static void main(String[] args){
	int a = 10;
	int b = 10;
	System.out.println(a > b);//false
	System.out.println(a >= b);//true
	System.out.println(a < b);//false
	System.out.println(a <= b);//true
	System.out.println(a == b);//true
	System.out.println(a != b);//false
	}
}